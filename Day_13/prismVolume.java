package New_13;

public class prismVolume {
    double base;
    double height;
    double length;

    public void setBase(double base) {
        this.base = base;
    }
    public void setHeight(double height){
        this.height=height;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double getBase() {
        return base;
    }

    public double getHeight() {
        return height;
    }

    public double getLength() {
        return length;
    }

}
